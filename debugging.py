#### Debugging instructions for spyder ####
# Open program file and create break points where you want to debug
# From Debug drop down > select Debug
# From menu > select Run Current line (this is the same as "Step")
# Watch terminal output
# To debug an object, select Step into Function
# From the object file, navigate to the variable explorer window on the right side
# From variable explorer window, double click object to see data attributes assigned

